// === دوال مساعدة ===

/**
 * الحصول على موديلات ماركة معينة
 */
export const getModelsByBrand = (brandName: string): string[] => {
    const brand = carBrands.find((b) => b.name === brandName);
    return brand ? brand.models : [];
};

/**
 * الحصول على جميع أسماء الماركات
 */
export const getAllBrandNames = (): string[] => {
    return carBrands.map((brand) => brand.name);
};

/**
 * الحصول على أسماء المدن فقط
 */
export const getCityNames = (): string[] => {
    return libyanCities.map((city) => city.name);
};

/**
 * الحصول على المدن الرئيسية فقط
 */
export const getMainCities = (): LibyanCity[] => {
    return libyanCities.filter((city) => city.isMainCity);
};

/**
 * الحصول على المدن حسب المنطقة
 */
export const getCitiesByRegion = (region: string): LibyanCity[] => {
    return libyanCities.filter((city) => city.region === region);
};

/**
 * الحصول على جميع المناطق
 */
export const getAllRegions = (): string[] => {
    return [...new Set(libyanCities.map((city) => city.region))];
};

